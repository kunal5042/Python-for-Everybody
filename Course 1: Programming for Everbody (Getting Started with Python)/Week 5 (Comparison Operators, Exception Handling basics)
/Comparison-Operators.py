variable1 = input("Enter the first number.\n")

variable2 = input("Enter the second number.\n")


if int(variable1) > int(variable2):
    print("\nFirst number is greater than the second number.")
          
if int(variable1) < int(variable2):
    print("\nFirst number is lesser than the second number.")
          
if int(variable1) == int(variable2):
    print("\nFirst number is equal to the second number.")




if int(variable1) != int(variable2):
    print("Both numbers are different.")

else:
    print("Both numbers are same.")